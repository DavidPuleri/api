package com.ekinox.entity;

public class LiveEvent {

  private Integer id;

  private String contenu;

  public LiveEvent() {

  }

  public Integer getId() {
    return id;
  }

  public void setId(Integer id) {
    this.id = id;
  }

  public String getContenu() {
    return contenu;
  }

  public void setContenu(String contenu) {
    this.contenu = contenu;
  }
}
